using System.ComponentModel.DataAnnotations;

namespace ChapterInventory.Models
{
    public class LoginUser
    {
        [Key]
        public int Id { get; set; }

        [Required]
        [StringLength(50)]
        public string Username { get; set; }

        [Required]
        [StringLength(50)]
        public string Password { get; set; }
    }
}
